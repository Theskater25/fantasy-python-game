def print_ascii(art):
    """Affiche un ASCII art en l'adaptant Г  la largeur du terminal."""
    try:
        term_width = os.get_terminal_size().columns
    except OSError:
        term_width = 80  # valeur par dГ©faut si pas de terminal dГ©tectГ©

    lines = art.split('\n')
    art_width = max((len(line) for line in lines), default=0)

    if art_width > term_width:
        # Terminal trop Г©troit : on prГ©vient et on tronque chaque ligne
        print(f"[ Agrandissez votre terminal pour un meilleur affichage ({art_width} colonnes recommandГ©es) ]")
        for line in lines:
            print(line[:term_width])
    else:
        # Terminal assez large : on centre l'ASCII art
        pad = (term_width - art_width) // 2
        for line in lines:
            print(' ' * pad + line)

