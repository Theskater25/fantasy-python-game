        # On compare l'XP de la base de donnГ©e et on ajoute l'XP gagnГ©e a lXP deja dans la base
        total_exp_after = player['exp'] + xp
        new_level = 1 + (total_exp_after // 20)
        if new_level > player['niveau']:
            levels_gained = new_level - player['niveau']
            print(f"\nвңЁ NIVEAU ! Vous montez de {levels_gained} niveau(s) : {player['niveau']} -> {new_level}")
